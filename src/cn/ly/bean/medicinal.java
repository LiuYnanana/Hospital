package cn.ly.bean;

public class medicinal {
	   private String medicinalId;
	   private String medicinalName;
	   private String medicinalNumber;
	   private String medicinalPrice;
	   private String medicinalDescription;
	   
	   public String getMedicinalId() {
		   return medicinalId;
	   }
	   public String getMedicinalName() {
		   return medicinalName;
	   }
	   public String getMedicinalNumber() {
		   return medicinalNumber;
	   }
	   public String getMedicinalPrice() {
		   return medicinalPrice;
	   }
	   public String getMedicinalDescription() {
		   return medicinalDescription;
	   }
	   
	   public void setMedicinalId(String medicinalId) {
		   this.medicinalId = medicinalId;
	   }
	   public void setMedicinalName(String medicinalName) {
		   this.medicinalName = medicinalName;
	   }
	   public void setMedicinalNumber(String medicinalNumber) {
		   this.medicinalNumber = medicinalNumber;
	   }
	   public void setMedicinalPrice(String medicinalPrice) {
		   this.medicinalPrice = medicinalPrice;
	   }
	   public void setMedicinalDescription(String medicinalDescription) {
		   this.medicinalDescription = medicinalDescription;
	   }
}
